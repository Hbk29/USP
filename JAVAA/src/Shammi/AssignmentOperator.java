package Shammi;

public class AssignmentOperator {

	public static void main(String[] args) {
		//when we have to assign values more than or less than 1
		
		int a=10;
		
		a+=4;
		System.out.println(a);
		
		a-=5;
		System.out.println(a);
		
		a*=2;
		System.out.println(a);
		
		a/=2;
		System.out.println(a);

	}

}
