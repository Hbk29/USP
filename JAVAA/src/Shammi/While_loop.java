package Shammi;

public class While_loop {

	public static void main(String[] args) {
		//while loop is used when we dont know the no of iterations.
		// its also called as entry control loop
		
		int n=1;     //start point
		
		while(n<=10)   //end point 
		{
			n++;       //increment
			System.out.println("while loop");
		}
		
		int b=1;
		 while(b<=35);
		 {
			 b++;
			 System.out.println("b variable");
		 }
		

	}

}
