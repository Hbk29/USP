package Shammi;

public class ForLoop {

	public static void main(String[] args) {
	
		int a[]= {1,2,3,4,5,6};
		
		for (int i=2;i<=6;i++)    //2,3,4,5,6
		{
			System.out.println(i);
		}
		
		for (int i=5;i<20;i++)    //5,6,7....19
		{
			System.out.println("value="+i);
		}
	
	}

}
